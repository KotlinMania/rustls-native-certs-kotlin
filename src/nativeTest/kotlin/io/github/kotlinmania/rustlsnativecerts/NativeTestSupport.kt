internal fun createTempRoot(name: String): Path {
    val path = Path("build/tmp/rustls-native-certs-tests/$name-${Random.nextLong().toULong().toString(16)}")
    deleteRecursively(path)
    SystemFileSystem.createDirectories(path)
    return path
}

internal fun writeTextFile(path: Path, text: String) {
    path.parent?.let(SystemFileSystem::createDirectories)
    SystemFileSystem.sink(path).buffered().use { sink -> sink.writeString(text) }
}

internal fun deleteRecursively(path: Path) {
    val metadata = SystemFileSystem.metadataOrNull(path) ?: return
    if (metadata.isDirectory) {
        for (child in SystemFileSystem.list(path)) {
            deleteRecursively(child)
        }
    }
    SystemFileSystem.delete(path, mustExist = false)
}
